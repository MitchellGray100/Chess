package com.robotchad.chess.client.location;

public class LocationImpl
{
    int xAxis;
    int yAxis;

    public LocationImpl() {

    }

    public int getXAxis()
    {
        return xAxis;
    }

    public int getYAxis()
    {
        return yAxis;
    }

    public void setXAxis(int x)
    {
        xAxis = x;
    }

    public void setYAxis(int y)
    {
        yAxis = y;
    }
}
